public class ExampleTwo {

	public static void main(String[] args) {
		int x = 8;
		double y = 7.2;
		
		System.out.println("x: " + x);  // prints x: 8
		System.out.println("y: " + y);  // prints y: 7.2
		System.out.println();
		
		x += 3;
		y -= 2;
		
		System.out.println("x: " + x);  // prints x: 11
		System.out.println("y: " + y);  // prints y: 5.2
		System.out.println();
		
		x *= 2.1;
		y /= -2;
		
		System.out.println("x: " + x);  // prints x: 11
		System.out.println("y: " + y);  // prints y: 5.2
		System.out.println();
		
		int z = 5;
		z %= 2;
		
		System.out.println("x: " + x);  // prints x: 11
		System.out.println("y: " + y);  // prints y: 5.2
		System.out.println("z: " + z);  // prints z: 1 

		System.out.println();
		
		
		
		

	}

}
